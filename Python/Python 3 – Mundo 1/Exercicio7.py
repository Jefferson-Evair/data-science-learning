# Desenvolva um programa que leia as duas notas de um aluno, calcule e mostre a sua média. 

nota1 = float(input('Infome a primeira nota:'))
nota2 = float(input('Infomer a segunda nota:'))

print('A primeira nota informada foi {} e a segunda foi {}, a média é {}'.format(nota1, nota2, (nota1 + nota2) / 2))